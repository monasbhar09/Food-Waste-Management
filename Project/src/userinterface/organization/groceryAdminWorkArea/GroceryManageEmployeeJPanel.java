    private void populateOrganizationJTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);
        EmployeeDirectory employeeDirectory = groceryOrganization.getEmployeeDirectory();
        for (Employee employee : employeeDirectory.getEmployeeList()) {
            Object[] row = new Object[3];
            row[0] = employee.getEmployeeId();
            row[1] = employee;
            row[2] = employee.getRole();
            model.addRow(row);
        }
    }

    private void populateRoleJCombobox() {
        roleJComboBox.removeAllItems();
        for (Role role : groceryOrganization.getSupportedRole()) {
            if (!role.getRoleType().getValue().equalsIgnoreCase(Role.RoleType.GroceryAdmin.getValue())) {
                roleJComboBox.addItem(role);
            }
        }
    }
