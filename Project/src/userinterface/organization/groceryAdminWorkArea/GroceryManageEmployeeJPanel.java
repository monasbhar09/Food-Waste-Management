    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        String name = employeeNameTextField.getText().trim();
        if (!name.isEmpty()) {
            Role role = (Role) roleJComboBox.getSelectedItem();
            groceryOrganization.getEmployeeDirectory().createEmployee(name, role);
            populateOrganizationJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please provide employee name!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_addJButtonActionPerformed

    private void removeEmployeeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeEmployeeBtnActionPerformed
        int selectedRow = organizationJTable.getSelectedRow();
        if (selectedRow >= 0) {
            Role role;
            UserAccount ua = (UserAccount) organizationJTable.getValueAt(selectedRow, 1);
            Employee employee = ua.getEmployee();

            groceryOrganization.getEmployeeDirectory().removeEmployee(employee);
            groceryOrganization.getUserAccountDirectory().removeUserAccount(ua);
            populateOrganizationJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_removeEmployeeBtnActionPerformed

    private void organizationJTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_organizationJTableMouseClicked
        int selectedRow = organizationJTable.getSelectedRow();
        Employee employee = (Employee) organizationJTable.getValueAt(selectedRow, 1);
        if (employee.getRole().getRoleType().equals(Role.RoleType.GroceryAdmin)) {
            removeEmployeeBtn.setEnabled(false);
        } else {
            removeEmployeeBtn.setEnabled(true);
        }
    }//GEN-LAST:event_organizationJTableMouseClicked


