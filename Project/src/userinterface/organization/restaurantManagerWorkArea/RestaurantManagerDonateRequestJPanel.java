    private void populateNGOJComboBox() {
        ngoJComboBox.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise instanceof EndPointEnterprise) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof NGOOrganization) {
                        ngoJComboBox.addItem(organization);
                    }

                }
                break;
            }
        }
    }

    private void populateFoodOfferWorkRequestJTable() {
        DefaultTableModel model = (DefaultTableModel) foodOfferWorkRequestJTable.getModel();

        model.setRowCount(0);

        for (UserAccount ua : restaurantOrganization.getUserAccountDirectory().getUserAccountList()) {
            for (FoodOfferWorkRequest workRequest : ua.getFoodOfferWorkQueue().getFoodOfferWorkRequestList()) {
                workRequest.checkExpired();
                if (workRequest.getStatus().equals(StatusEnum.RestaurantToSell)) {
                    Object[] row = new Object[5];
                    row[0] = workRequest.getProduct();
                    row[1] = workRequest.getTotalQuantity();
                    row[2] = workRequest.getAvailQuantity();
                    row[3] = workRequest.getSender();
                    row[4] = workRequest;
                    model.addRow(row);
                }
            }
        }
    }

