    private void donatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_donatejButtonActionPerformed
        int selectedRow = foodOfferWorkRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }

        FoodOfferWorkRequest selectedRequest = (FoodOfferWorkRequest) foodOfferWorkRequestJTable.getValueAt(selectedRow, 4);
        Organization selectedNGO = (NGOOrganization) ngoJComboBox.getSelectedItem();
        selectedNGO.getFoodOfferWorkQueue().getFoodOfferWorkRequestList().add(selectedRequest);
        selectedRequest.setStatus(StatusEnum.RestaurantToDonate);
        JOptionPane.showMessageDialog(null, "Request sent to " + selectedNGO + ". Thank You!!");
        populateFoodOfferWorkRequestJTable();
    }//GEN-LAST:event_donatejButtonActionPerformed


