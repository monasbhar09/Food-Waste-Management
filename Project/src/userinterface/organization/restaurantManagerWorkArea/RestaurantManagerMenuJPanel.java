    private void populateProductJTable() {
        DefaultTableModel model = (DefaultTableModel) productJTable.getModel();

        model.setRowCount(0);

        for (Product product : restaurantOrganization.getProductCatalog().getProductCatalog()) {
            Object[] row = new Object[2];
            row[0] = product.getProductId();
            row[1] = product;
            model.addRow(row);
        }
    }
