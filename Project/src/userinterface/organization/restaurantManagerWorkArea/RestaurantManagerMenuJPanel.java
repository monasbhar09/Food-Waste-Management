    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = productJTable.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the product?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Product product = (Product) productJTable.getValueAt(selectedRow, 1);
                restaurantOrganization.getProductCatalog().deleteProduct(product);
                populateProductJTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!", " ",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void addBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtn1ActionPerformed
        // TODO add your handling code here:
        String productName = productNamejTextField.getText();
        if (!productName.isEmpty()) {
            Product product = restaurantOrganization.getProductCatalog().addProduct();
            product.setProductName(productName);
            populateProductJTable();
            productNamejTextField.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Please provide product name!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_addBtn1ActionPerformed


