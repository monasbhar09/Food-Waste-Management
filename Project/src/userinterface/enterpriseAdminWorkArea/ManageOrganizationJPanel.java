    private void populateOrganizationTable() {
        DefaultTableModel model = (DefaultTableModel) networkTable.getModel();
        model.setRowCount(0);

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationName();
            String type = null;
            if (organization instanceof RestaurantOrganization) {
                type = OrganizationType.Restaurant.getOrganization();
            } else if (organization instanceof GroceryOrganization) {
                type = OrganizationType.Grocery.getOrganization();
            } else if (organization instanceof HouseholdOrganization) {
                type = OrganizationType.Household.getOrganization();
            } else if (organization instanceof NGOOrganization) {
                type = OrganizationType.NGO.getOrganization();
            }
            row[1] = type;
            model.addRow(row);
        }
    }

    private void populateOrganizationTypeJCombo() {
        organizationTypejComboBox.removeAllItems();
        if (enterprise instanceof FoodManagementEnterprise) {
            for (OrganizationType type : OrganizationType.values()) {
                if (!(type.getOrganization().equalsIgnoreCase("Household Organization")) && type.getEnterprise().equals("FME")) {
                    organizationTypejComboBox.addItem(type);
                }
            }
        } else if (enterprise instanceof EndPointEnterprise) {
            for (OrganizationType type : OrganizationType.values()) {
                if (type.getEnterprise().equals("EPE")) {
                    organizationTypejComboBox.addItem(type);
                }
            }
        }
    }
