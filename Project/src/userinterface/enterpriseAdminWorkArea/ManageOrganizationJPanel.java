    private void addOrganizationBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrganizationBtnActionPerformed
        String organizationName = organizationNameTextField.getText().trim();
        if (!organizationName.isEmpty()) {
            OrganizationType type = (OrganizationType) organizationTypejComboBox.getSelectedItem();
            enterprise.getOrganizationDirectory().createOrganization(organizationName, type);
            populateOrganizationTable();
            organizationNameTextField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please provide organization name!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_addOrganizationBtnActionPerformed


