    private void addNetworkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNetworkBtnActionPerformed
        String networkName = networkNameTextField.getText().trim();
        if (!networkName.isEmpty()) {
            Network network = system.getNetworkList().createAndAddNetwork(networkName);
            network.setNetworkName(networkName);
            populateNetworkTable();
            networkNameTextField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please provide network name!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

    }//GEN-LAST:event_addNetworkBtnActionPerformed


