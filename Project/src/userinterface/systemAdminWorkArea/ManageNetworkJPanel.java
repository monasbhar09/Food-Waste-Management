    private void populateNetworkTable() {
        DefaultTableModel model = (DefaultTableModel) networkTable.getModel();

        model.setRowCount(0);

        for (Network network : system.getNetworkList().getNetworkList()) {
            Object[] row = new Object[1];
            row[0] = network.getOrganizationName();
            model.addRow(row);
        }
    }
